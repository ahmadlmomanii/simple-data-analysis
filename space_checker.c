#include "push_swap.h"

int	has_space(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == ' ' || s[i] == '\t' || s[i] == '\n' || s[i] == '\r'
			|| s[i] == '\v' || s[i] == '\f')
			return (1);
		i++;
	}
	return (0);
}
